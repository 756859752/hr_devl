package pojo;

public class Color {
	private int colorid;
	private String colorname;
	public int getColor_id() {
		return colorid;
	}
	public void setColor_id(int color_id) {
		this.colorid = color_id;
	}
	public String getColor_name() {
		return colorname;
	}
	public void setColor_name(String color_name) {
		this.colorname = color_name;
	}
}
